defined('_JEXEC') or die;

$id = '';

if ($tagId = $params->get('tag_id', ''))
{
	$id = ' id="' . $tagId . '"';
}

// The menu class is deprecated. Use nav instead
?>
<div class="row cardmenu <?php echo $class_sfx; ?> mod-list"<?php echo $id; ?>>

<?php 

$itemcount = 0;

foreach ($list as $i => &$item)
{
	$itemcount++;
}

foreach ($list as $i => &$item)
{
	$class = 'item-' . $item->id;

	if ($item->id == $default_id)
	{
		$class .= ' default';
	}

	if ($item->id == $active_id || ($item->type === 'alias' && $item->params->get('aliasoptions') == $active_id))
	{
		$class .= ' current';
	}

	if (in_array($item->id, $path))
	{
		$class .= ' active';
	}
	elseif ($item->type === 'alias')
	{
		$aliasToId = $item->params->get('aliasoptions');

		if (count($path) > 0 && $aliasToId == $path[count($path) - 1])
		{
			$class .= ' active';
		}
		elseif (in_array($aliasToId, $path))
		{
			$class .= ' alias-parent-active';
		}
	}

	if ($item->type === 'separator')
	{
		$class .= ' divider';
	}

	if ($item->deeper)
	{
		$class .= ' deeper';
	}

	if ($item->parent)
	{
		$class .= ' parent';
	}

	if ($itemcount > 3){
		echo '<div class="col-6 col-lg-3 my-2 ' . $class . '"><div class="h-100 cardmenu-item">';

	} else {
		echo '<div class="col-12 col-lg-4 my-2 ' . $class . '"><div class="h-100 cardmenu-item">';
		
	}

	switch ($item->type) :
		case 'separator':
		case 'component':
		case 'heading':
		case 'url':
			require JModuleHelper::getLayoutPath('mod_menu', 'card_' . $item->type);
			break;

		default:
			require JModuleHelper::getLayoutPath('mod_menu', 'card_url');
			break;
	endswitch;

	echo '</div></div>';


}

?>
</div>
